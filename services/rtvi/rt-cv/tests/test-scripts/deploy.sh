# Deploy the RT-CV + Kafka docker compose stack.
# Exit 0 on success, 1 on failure.

set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
# shellcheck source=lib/common.sh
source "${SCRIPT_DIR}/lib/common.sh"
load_env

echo "=== Deploying compose stack (${COMPOSE_DIR}) ==="
compose up -d

wait_for_kafka_healthy
wait_for_rest

test_pass "Compose stack deployed (Kafka healthy, REST reachable at ${REST_URL})"
